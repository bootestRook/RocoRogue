export function requiredPetExpForNextLevel(level) {
  const numericLevel = Number(level);
  if (!Number.isFinite(numericLevel)) return 0;

  const normalizedLevel = Math.max(
    PET_LEVEL_EXP_SPEC.minLevel,
    Math.min(PET_LEVEL_EXP_SPEC.maxLevel, Math.round(numericLevel)),
  );
  return PET_NEXT_LEVEL_EXP_BY_LEVEL[normalizedLevel] ?? 0;
}
